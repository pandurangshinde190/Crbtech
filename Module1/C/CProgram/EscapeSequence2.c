 #include<stdio.h>
 int main(void)
 {
     printf("Hello,\nWord!!\n\n");

     printf("Hell\bo,Word!!\n");

     printf("Hello,\\nWord!!\n");
     
     printf("\"Hello,Word!!\"\n\n");

     printf("Hello,\tWord!!\n\n");

     printf("Hello,\vWord!!\n\n");

     printf("Hello,\aWord!!\n\n");

     printf("\n\nHello,\\Word!!");

     printf("\n\nHello,Word!!\r");

     return 0;
 }


